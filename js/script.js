function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

console.log(`add(3, 2): ${add(3, 2)}`)
console.log(`subtract(3, 2): ${subtract(3, 2)}`);
console.log(`multiply(3, 2): ${multiply(3, 2)}`);
console.log(`divide(3, 2): ${divide(3, 2)}`);
